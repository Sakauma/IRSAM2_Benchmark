#!/usr/bin/env bash
# Author: Egor Izmaylov
#
# AutoDL 服务器初始化脚本。
# 这份脚本默认采用下面的目录策略：
# - /root/autodl-fs: 保存压缩包和长期备份
# - /root/autodl-tmp: 保存解压后的数据集、运行输出、checkpoint
#
# 默认会寻找两个压缩包：
# - MultiModalCOCOClean.zip
# - RBGT-Tiny.tar.gz
#
# 运行结束后会生成项目根目录下的 .autodl_env.sh，后续可以直接 source 使用。

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "${SCRIPT_DIR}/.." && pwd)"

AUTO_TMP_ROOT="${AUTO_TMP_ROOT:-/root/autodl-tmp}"
AUTO_FS_ROOT="${AUTO_FS_ROOT:-/root/autodl-fs}"
DATASET_ARCHIVE_ROOT="${DATASET_ARCHIVE_ROOT:-${AUTO_FS_ROOT}}"

BENCH_DATASET_ROOT="${BENCH_DATASET_ROOT:-${AUTO_TMP_ROOT}/datasets}"
ARTIFACT_ROOT="${ARTIFACT_ROOT:-${AUTO_TMP_ROOT}/runs}"
CHECKPOINT_ROOT="${CHECKPOINT_ROOT:-${AUTO_TMP_ROOT}/checkpoints}"
SAM2_REPO="${SAM2_REPO:-/root/sam2}"

MULTIMODAL_ARCHIVE="${MULTIMODAL_ARCHIVE:-${DATASET_ARCHIVE_ROOT}/MultiModalCOCOClean.zip}"
RBGT_ARCHIVE="${RBGT_ARCHIVE:-${DATASET_ARCHIVE_ROOT}/RBGT-Tiny.tar.gz}"

MULTIMODAL_DIR="${BENCH_DATASET_ROOT}/MultiModalCOCOClean"
RBGT_DIR="${BENCH_DATASET_ROOT}/RBGT-Tiny"
ENV_FILE="${PROJECT_ROOT}/.autodl_env.sh"

mkdir -p "${BENCH_DATASET_ROOT}" "${ARTIFACT_ROOT}" "${CHECKPOINT_ROOT}"

extract_zip() {
  local archive_path="$1"
  local target_root="$2"
  if command -v unzip >/dev/null 2>&1; then
    unzip -q "${archive_path}" -d "${target_root}"
  else
    python -m zipfile -e "${archive_path}" "${target_root}"
  fi
}

if [[ -f "${MULTIMODAL_ARCHIVE}" && ! -d "${MULTIMODAL_DIR}" ]]; then
  echo "[setup] extracting ${MULTIMODAL_ARCHIVE}"
  extract_zip "${MULTIMODAL_ARCHIVE}" "${BENCH_DATASET_ROOT}"
fi

if [[ -f "${RBGT_ARCHIVE}" && ! -d "${RBGT_DIR}" ]]; then
  echo "[setup] extracting ${RBGT_ARCHIVE}"
  tar -xzf "${RBGT_ARCHIVE}" -C "${BENCH_DATASET_ROOT}"
fi

cat > "${ENV_FILE}" <<EOF
#!/usr/bin/env bash
# Author: Egor Izmaylov
# Auto-generated by scripts/setup_autodl_server.sh

export SAM2_REPO="${SAM2_REPO}"
export CHECKPOINT_ROOT="${CHECKPOINT_ROOT}"
export SAM2_CKPT_ROOT="\${SAM2_CKPT_ROOT:-${CHECKPOINT_ROOT}}"
export ARTIFACT_ROOT="${ARTIFACT_ROOT}"
export BENCH_DATASET_ROOT="${BENCH_DATASET_ROOT}"
export MULTIMODAL_DATASET_ROOT="${MULTIMODAL_DIR}"
export RBGT_DATASET_ROOT="${RBGT_DIR}"
export PYTHON_BIN="\${PYTHON_BIN:-python}"
EOF

chmod +x "${ENV_FILE}"

echo
echo "[setup] generated environment file: ${ENV_FILE}"
echo "[setup] MultiModal dataset root: ${MULTIMODAL_DIR}"
echo "[setup] RBGT-Tiny dataset root: ${RBGT_DIR}"
echo
echo "[next] source ${ENV_FILE}"
echo "[next] bash scripts/run_autodl_smoke.sh multimodal sam2_zero_shot"
echo "[next] bash scripts/run_autodl_smoke.sh rbgt sam2_zero_shot"
